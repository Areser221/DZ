n = int(input("Введите количество чисел: "))
a = input('Введите значения целых чисел через пробел: ').split()
x=[]
for i in range(len(a)):
    x.append(int(a[i]))
cnt = 0
if len(x)!=n:
    print("Вы где то ошиблись в вводе данных, так как n - количество чисел во второй строке")
if len(x)==0:
    print("Вы ничего не ввели")
# prev2 — максимальная сумма, если бы массив закончился 2 шага назад
prev2 = 0
# prev1 — максимальная сумма, если бы массив закончился 1 шаг назад
prev1 = 0

# Мы просто берём и по очереди перебираем каждое число из нашего списка x
for num in x:
    # У нас есть всего два выбора для текущего числа 'num':
    # 1. Мы НЕ берём его. Тогда лучшая сумма остаётся такой же, как шаг назад (prev1)
    # 2. Мы БЕРЁМ его. Тогда мы прибавляем его к сумме, которая была 2 шага назад (prev2 + num)
    current_max = max(prev1, prev2 + num)
    
    # Шагаем вперёд: старый прошлый шаг становится позапрошлым, а текущий — прошлым
    prev2 = prev1
    prev1 = current_max

# В prev1 гарантированно окажется правильный максимальный ответ для всего массива!
print(prev1)
